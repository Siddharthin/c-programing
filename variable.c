#include <stdio.h>

int main()  {
    int num = 22;
    char star = 's';
    int age = 199;
    float pi = 3.14f ;

    printf("num  is %d\n", num);
    printf("star is %c\n", star);
    printf("age is %d\n",age);
    printf("pi is %f\n", pi);
    printf("this are format specifiers in C language");

    return 0;
 
}